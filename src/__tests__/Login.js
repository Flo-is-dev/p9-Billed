// * Nouveau bloc de tests ajouté pour vérifier que lorsqu'un utilisateur remplit les champs correctement et soumet le formulaire de connexion, il est identifié en tant qu'employé avec un store défini.
describe("Given that I am a user on login page", () => {
  describe("When I fill fields in correct format and I click on employee button Login In", () => {
    test("Then I should be identified as an Employee in app with a defined store", async () => {
      document.body.innerHTML = LoginUI();
      const inputData = {
        email: "johndoe@email.com",
        password: "azerty",
      };

      //   * Test pour vérifier que le champ de saisie de l'email est correctement modifié avec l'email fourni dans inputData.

      const inputEmailUser = screen.getByTestId("employee-email-input");
      fireEvent.change(inputEmailUser, { target: { value: inputData.email } });
      expect(inputEmailUser.value).toBe(inputData.email);

      //   * Test pour vérifier que le champ de mot de passe est correctement modifié avec le mot de passe fourni dans inputData.

      const inputPasswordUser = screen.getByTestId("employee-password-input");
      fireEvent.change(inputPasswordUser, {
        target: { value: inputData.password },
      });
      expect(inputPasswordUser.value).toBe(inputData.password);

      //   * Commentaire : Sélectionne le formulaire de connexion de l'employé pour la soumission ultérieure.
      const form = screen.getByTestId("form-employee");

      //  * Mock du localStorage pour simuler le stockage des données utilisateur sans toucher au véritable localStorage du navigateur.
      Object.defineProperty(window, "localStorage", {
        value: {
          getItem: jest.fn(() => null),
          setItem: jest.fn(() => null),
        },
        writable: true,
      });

      //   * Commentaire : Mock de la fonction onNavigate pour simuler la navigation lors de la soumission du formulaire et mettre à jour l'interface utilisateur avec la route correspondante.
      const onNavigate = (pathname) => {
        document.body.innerHTML = ROUTES({ pathname });
      };

      let PREVIOUS_LOCATION = "";

      //   * Création d'un mock store pour simuler une réponse réussie de la méthode login du store avec un jeton JWT simulé.
      const mockStore = {
        login: jest.fn(() => Promise.resolve({ jwt: "mockJwt" })),
      };

      const login = new Login({
        document,
        localStorage: window.localStorage,
        onNavigate,
        PREVIOUS_LOCATION,
        store: mockStore,
      });

      //   *  Mock de la méthode handleSubmitEmployee et ajout d'un écouteur d'événements pour déclencher cette méthode lors de la soumission du formulaire.
      const handleSubmit = jest.fn(login.handleSubmitEmployee);
      form.addEventListener("submit", handleSubmit);

      //   * Simule la soumission du formulaire de connexion pour tester le comportement du code.
      fireEvent.submit(form);

      //   * Vérifie que la méthode login du store est appelée avec les bonnes données (email et password de l'utilisateur) après la soumission du formulaire.
      expect(mockStore.login).toHaveBeenCalledWith(
        JSON.stringify({
          email: inputData.email,
          password: inputData.password,
        })
      );
    });
  });
});
